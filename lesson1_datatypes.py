# 變數的命名規則：使用有意義的名稱，並遵循蛇形命名法 (snake_case)
# 例如：my_name, my_age, is_active

# 變數的定義與賦值
# 變數名稱 = 值

# 整數 (Integer)
my_age = 25

# 浮點數 (Float)
my_height = 175.5

# 字串 (String)
my_name = "小明"

# 布林值 (Boolean)
is_student = True

# 使用 print() 函數來顯示變數的內容
print("姓名:", my_name)
print("年齡:", my_age)
print("身高:", my_height)
print("學生身分:", is_student)

# 使用 type() 函數來檢查變數的資料型態
print("my_name 的資料型態是:", type(my_name))
print("my_age 的資料型態是:", type(my_age))
print("my_height 的資料型態是:", type(my_height))
print("is_student 的資料型態是:", type(is_student))