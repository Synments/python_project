# --- for 迴圈範例 ---
print("--- for 迴圈範例 ---")

# 使用 for 迴圈，從 0 到 4 進行迭代
for i in range(5):
    print("數字是:", i)

# --- while 迴圈範例 ---
print("\n--- while 迴圈範例 ---")

# 設定一個初始變數
count = 0

# 當 count 小於 5 時，重複執行迴圈內的程式碼
while count < 5:
    print("數字是:", count)
    # 每執行一次，將 count 的值加 1
    count = count + 1

print("迴圈結束。")